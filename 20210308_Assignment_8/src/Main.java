// Main.java -------------------------------------------------------------------
import java.text.DecimalFormat;
import java.util.Arrays;
import java.util.Random;

import cs_1c.MySort;

public class Main
{
   public static void main(String[] args)
   {
      final int ARRAY_SIZE = 20000;

      System.out.println("array size = " + ARRAY_SIZE + "\n");

      Integer[] array = new Integer[ARRAY_SIZE];
      Random random = new Random();

      for (int i = 0; i < ARRAY_SIZE; i++)
      {
         array[i] = random.nextInt(ARRAY_SIZE);
      }

      for (int recursionLimit = 2; recursionLimit <= 300; recursionLimit += 2)
      {
         MySort.setRecursionLimit(recursionLimit);

         Integer[] copy = Arrays.copyOf(array, ARRAY_SIZE);

         long startTime = System.nanoTime();
         MySort.quickSort(copy);
         long stopTime = System.nanoTime();

         DecimalFormat format = new DecimalFormat("0");

         System.out.println(recursionLimit + " : "
               + format.format((stopTime - startTime) / 1e6) + " ms");

         // System.out.println(Arrays.toString(copy));

      }
   }
}

